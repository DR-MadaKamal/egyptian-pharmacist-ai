export function normalizeManufacturer(name) {
  if (!name || !name.trim()) return ''
  let n = name.trim()
  const upper = n.toUpperCase()
  // Direct lookup (case-insensitive)
  if (KNOWN_CORRECTIONS[upper]) return KNOWN_CORRECTIONS[upper]
  // Strip common suffixes
  for (const pattern of SUFFIXES) {
    n = n.replace(pattern, '')
  }
  // Normalize
  n = n.replace(/\s*\(.*?\)/g, '')
    .replace(/\s*\[.*?\]/g, '')
    .replace(/["""''']/g, '')
    .replace(/\s*[-,/\\]\s*/g, ' ')
    .replace(/\s{2,}/g, ' ')
    .trim()
  // Check again after normalization
  const normUpper = n.toUpperCase()
  if (KNOWN_CORRECTIONS[normUpper]) return KNOWN_CORRECTIONS[normUpper]
  return n || name.trim()
}
