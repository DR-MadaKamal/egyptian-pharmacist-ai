export function getInsulinsByCategory(categoryId) {
  return INSULINS.filter(i => i.category === categoryId)
}

export function getInsulinById(id) {
  return INSULINS.find(i => i.id === id)
}

export function searchInsulins(query) {
  const q = query.toLowerCase().trim()
  if (!q) return INSULINS
  return INSULINS.filter(i =>
    i.nameEn.toLowerCase().includes(q) ||
    i.nameAr.includes(q) ||
    i.genericEn.toLowerCase().includes(q) ||
    i.genericAr.includes(q) ||
    i.manufacturer.toLowerCase().includes(q) ||
    i.manufacturerAr.includes(q)
  )
}
